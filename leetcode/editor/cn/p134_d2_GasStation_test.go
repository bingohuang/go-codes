func canCompleteCircuit(gas []int, cost []int) int {
	// 2020-08-20 15:19 @bingohuang
	// ç®—æ³•ï¼šä¸€æ¬¡éå†
	// https://leetcode-cn.com/problems/gas-station/solution/jia-you-zhan-by-leetcode/
	// å¤æ‚åº¦ï¼šO(N)/O(1)
	// æ•ˆç‡ï¼šæ‰§è¡Œè€—æ—¶:4 ms,å‡»è´¥äº†96.84% çš„Goç”¨æˆ·
	//			å†…å­˜æ¶ˆè€—:3 MB,å‡»è´¥äº†60.38% çš„Goç”¨æˆ·
	n := len(gas)
	total_tank, curr_tank, ans := 0, 0, 0
	for i := 0; i < n; i++ {
		total_tank += gas[i] - cost[i]
		curr_tank += gas[i] - cost[i]
		if curr_tank < 0 {
			ans = i + 1
			curr_tank = 0
		}
	}
	if total_tank >= 0 {
		return ans
	}
	return -1
}

