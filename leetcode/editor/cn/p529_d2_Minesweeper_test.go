func updateBoard(board [][]byte, click []int) [][]byte {
	// 2020-08-20 09:33 @bingohuang
	// ç®—æ³•ï¼šæ·±åº¦ä¼˜å…ˆæœç´¢+æ¨¡æ‹Ÿ
	// å¤æ‚åº¦ï¼šO(M*N)/O(M*N)
	// æ•ˆç‡ï¼šæ‰§è¡Œè€—æ—¶:36 ms,å‡»è´¥äº†80.43% çš„Goç”¨æˆ·
	//		å†…å­˜æ¶ˆè€—:6.3 MB,å‡»è´¥äº†46.15% çš„Goç”¨æˆ·
	var dirX = []int{0, 1, 0, -1, 1, 1, -1, -1}
	var dirY = []int{1, 0, -1, 0, 1, -1, 1, -1}

	var dfs func([][]byte, int, int)
	dfs = func(board [][]byte, x, y int) {
		cnt := 0
		for i := 0; i < 8; i++ {
			tx, ty := x+dirX[i], y+dirY[i]
			if tx < 0 || tx >= len(board) || ty < 0 || ty >= len(board[0]) {
				continue
			}
			if board[tx][ty] == 'M' {
				cnt++
			}
		}
		if cnt > 0 {
			board[x][y] = byte(cnt + '0')
		} else {
			board[x][y] = 'B'
			for i := 0; i < 8; i++ {
				tx, ty := x+dirX[i], y+dirY[i]
				if tx < 0 || tx >= len(board) || ty < 0 || ty >= len(board[0]) || board[tx][ty] != 'E' {
					continue
				}
				dfs(board, tx, ty)
			}
		}
	}
	x, y := click[0], click[1]
	if board[x][y] == 'M' {
		board[x][y] = 'X'
	} else {
		dfs(board, x, y)
	}

	return board
}

