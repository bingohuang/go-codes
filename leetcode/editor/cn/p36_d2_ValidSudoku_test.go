func isValidSudoku(board [][]byte) bool {
	// 20200818
	// æ‰§è¡Œè€—æ—¶:4 ms,å‡»è´¥äº†66.43% çš„Goç”¨æˆ·
	// å†…å­˜æ¶ˆè€—:2.8 MB,å‡»è´¥äº†69.67% çš„Goç”¨æˆ·
	for i := 0; i < 9; i++ {
		m1 := map[byte]bool{}
		m2 := map[byte]bool{}
		m3 := map[byte]bool{}
		for j := 0; j < 9; j++ {
			// row
			if board[i][j] != '.' {
				if m1[board[i][j]] {
					return false
				}
				m1[board[i][j]] = true
			}
			// column
			if board[j][i] != '.' {
				if m2[board[j][i]] {
					return false
				}
				m2[board[j][i]] = true
			}
			// part
			row := (i%3)*3 + j%3
			col := (i/3)*3 + j/3
			if board[row][col] != '.' {
				if m3[board[row][col]] {
					return false
				}
				m3[board[row][col]] = true
			}
		}
	}
	return true
}

